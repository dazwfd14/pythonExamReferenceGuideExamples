#initial string to process
str = "Hello World"

# string[i] retrieves character at position i
# first letter
print("First character: ",str[0])

# string[-1] retrieves last character
print("Last character: ",str[-1])

# string[i:j] retrieves characters in range i to j
print("Characters in range 0:4: ",str[0:4])