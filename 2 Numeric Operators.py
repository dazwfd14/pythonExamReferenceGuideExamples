#Output headings
print("Numeric Operators:")
print("------------------\n")

x = 4
y = 3
result = 0

# + addition
result = x + y
print("x ", x, "\tplus\t\t\t y: ", y, "\t\tresult: ",result)

# - subtraction
result = x - y
print("x ", x, "\tsubtract\t\t y: ", y, "\t\tresult: ",result)

# * multiplication
result = x * y
print("x ", x, "\tmultiply\t\t y: ", y, "\t\tresult: ",result)

# / division
result = x / y
print("x ", x, "\tdivide \t\t\t y: ", y, "\t\tresult: ",result)

# ** exponent
result = x ** y
print("x ", x, "\tto the power of \t y: ", y, "\t\tresult: ",result)

# % modulus
result = x % y
print("x ", x, "\tmod\t\t\t y: ", y, "\t\tresult: ",result)

# // floor division
result = x // y
print("x ", x, "\tfloor division  \t y: ", y, "\t\tresult: ",result)